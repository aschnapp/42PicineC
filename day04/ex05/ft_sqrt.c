int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i < 46342)
	{
		if (i * i == nb)
		{
			return (i);
		}
		if (i * i > nb)
		{
			return (0);
		}
		i++;
	}
	return (0);
}
