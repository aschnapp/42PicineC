int		ft_antidote(int i, int j, int k)
{
	if (i >= j >= k || k >= j >= i)
		return (j);
	if (j >= i >= k || k >= i >= j)
		return (i);
	if (i >= k >= j || j >= k >= i)
		return (k);
	return (0);
}
